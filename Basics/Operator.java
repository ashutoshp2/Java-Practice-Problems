package Basics;

public class Operator {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        int sum = a+b;
        int diff = a-b;
        int mult = a*b;
        float div = a/b;
        int mod = a%b;

        System.out.println(sum);
        System.out.println(diff);
        System.out.println(mult);
        System.out.println(div);
        System.out.println(mod);
    }
}
